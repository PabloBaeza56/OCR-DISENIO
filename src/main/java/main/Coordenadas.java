package main;

public class Coordenadas {
    private float esquinaSuperiorIzquierda;
    private float esquinaSuperiorDerecha;
    private float esquinaInferiorIzquierda;
    private float esquinaInferiorDerecha;


    public Coordenadas(float esquinaSuperiorIzquierda, float esquinaSuperiorDerecha, float esquinaInferiorIzquierda, float esquinaInferiorDerecha ) {
        this.esquinaSuperiorIzquierda = esquinaSuperiorIzquierda;
        this.esquinaSuperiorDerecha = esquinaSuperiorDerecha;
        this.esquinaInferiorIzquierda = esquinaInferiorIzquierda;
        this.esquinaInferiorDerecha = esquinaInferiorDerecha;
    }

  
}



